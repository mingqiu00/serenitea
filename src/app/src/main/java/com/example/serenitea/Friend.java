package com.example.serenitea;

public class Friend {
    String nickname;
    String avatar_id;
    String dob;
    int cup_of_tea;
    String gender;

    public Friend(String nickname, String avatar_id, String dob, int cup_of_tea, String gender) {
        this.nickname = nickname;
        this.avatar_id = avatar_id;
        this.dob = dob;
        this.cup_of_tea = cup_of_tea;
        this.gender = gender;
    }
}
